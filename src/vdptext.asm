;==============================================================================
        call    vdp_init
        jp      exitpgm
; -----------------------------------------------------------------------------
vdp_init:
        ; Initialise screen as Graphics II Mode
        call    F_BIOS_VDP_SET_MODE_G2

        ; Copy Sprite data to VRAM
        ; ld      BC, 512
        ; ld      DE, font_patterns
        ; ld      HL, VDP_G2_PATT_TAB
        ; call    F_BIOS_VDP_LDIR_VRAM


; To fill the entire screen, we need to copy 3 * 2048 = 6144 bytes

        ; Copy 2048 bytes of font patterns
        ld      HL, VDP_G2_PATT_TAB
        call    F_BIOS_VDP_SET_ADDR_WR
        ld      C, 3                    ; outer outer loop (6144 / 2048 = 3)
loop_patrn_outer2:
        ld      IX, font_patterns
        ld      D, 8                    ; outer loop (2048 / 256 = 8)
loop_patrn_outer:
        ld      B, 0                    ; inner loop
loop_patrn_inner:
        ld      A, (IX)
        push    BC
        call    F_BIOS_VDP_BYTE_TO_VRAM
        pop     BC
        inc     IX
        djnz    loop_patrn_inner
        dec     D
        jr      nz, loop_patrn_outer
        dec     C
        jr      nz, loop_patrn_outer2

        ; Copy 2048 bytes of colour table
        ld      HL, VDP_G2_COLR_TAB
        call    F_BIOS_VDP_SET_ADDR_WR
        ld      C, 3                    ; outer outer loop (6144 / 2048 = 3)
loop_colr_outer2:
        ld      IX, colour_table
        ld      D, 8                    ; outer loop (2048 / 256 = 8)
loop_colr_outer:
        ld      B, 0                    ; inner loop
loop_clr_inner:
        ld      A, (IX)
        push    BC
        call    F_BIOS_VDP_BYTE_TO_VRAM
        pop     BC
        inc     IX
        djnz    loop_clr_inner
        dec     D
        jr      nz, loop_colr_outer
        dec     C
        jr      nz, loop_colr_outer2

        ret

;==============================================================================
; RETURN TO DZOS CLI
;==============================================================================
exitpgm:
        ld      HL, (CLI_prompt_addr)
        jp      (HL)                    ; return control to CLI

